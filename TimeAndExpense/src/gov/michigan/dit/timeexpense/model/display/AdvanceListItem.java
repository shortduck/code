package gov.michigan.dit.timeexpense.model.display;

import java.io.Serializable;
import java.util.Date;

public class AdvanceListItem implements Serializable {

	private static final long serialVersionUID = 3518524428959890130L;

	private int apptId;
	private int permAdvInd;
	private int advanceMasterId;
	private Date requestDate;
	private Date paidDate;
	private String reason;
	private Date fromDate;
	private Date toDate;
    private double amountRequested;
    private double amountOutstanding;
    private double amountLiquidated;
    private double amountManualDeposit;
    private int revisionNumber;
    private String status;
    private boolean enableDelete;
	
    
    public int getAdvanceMasterId() {
		return advanceMasterId;
	}
	public void setAdvanceMasterId(int advanceMasterId) {
		this.advanceMasterId = advanceMasterId;
	}
	public Date getRequestDate() {
		return requestDate;
	}
	public void setRequestDate(Date requestDate) {
		this.requestDate = requestDate;
	}
	public Date getPaidDate() {
		return paidDate;
	}
	public void setPaidDate(Date paidDate) {
		this.paidDate = paidDate;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public Date getFromDate() {
		return fromDate;
	}
	public void setFromDate(Date fromDate) {
		this.fromDate = fromDate;
	}
	public Date getToDate() {
		return toDate;
	}
	public void setToDate(Date toDate) {
		this.toDate = toDate;
	}
	public double getAmountRequested() {
		return amountRequested;
	}
	public void setAmountRequested(double amountRequested) {
		this.amountRequested = amountRequested;
	}
	public double getAmountOutstanding() {
		return amountOutstanding;
	}
	public void setAmountOutstanding(double amountOutstanding) {
		this.amountOutstanding = amountOutstanding;
	}
	public double getAmountLiquidated() {
		return amountLiquidated;
	}
	public void setAmountLiquidated(double amountLiquidated) {
		this.amountLiquidated = amountLiquidated;
	}
	public double getAmountManualDeposit() {
		return amountManualDeposit;
	}
	public void setAmountManualDeposit(double amountManualDeposit) {
		this.amountManualDeposit = amountManualDeposit;
	}
	public int getRevisionNumber() {
		return revisionNumber;
	}
	public void setRevisionNumber(int revisionNumber) {
		this.revisionNumber = revisionNumber;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public boolean isEnableDelete() {
		return enableDelete;
	}
	public void setEnableDelete(boolean enableDelete) {
		this.enableDelete = enableDelete;
	}
	public int getApptId() {
		return apptId;
	}
	public void setApptId(int apptId) {
		this.apptId = apptId;
	}
	public int getPermAdvInd() {
		return permAdvInd;
	}
	public void setPermAdvInd(int permAdvInd) {
		this.permAdvInd = permAdvInd;
	}
    
	
	
	
	
}
