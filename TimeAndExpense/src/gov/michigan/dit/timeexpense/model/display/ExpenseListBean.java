package gov.michigan.dit.timeexpense.model.display;

import java.io.Serializable;
import java.util.Date;

/**
 * Class to encapsulate the fields to be displayed on the expense transaction
 * listing page.
 * 
 * @author chaudharym
 */

public class ExpenseListBean implements Serializable{
	private static final long serialVersionUID = 3954334205011592635L;
	
	private int apptIdentifier;
	private int empIdentifier;
	private int expmIdentifier;
	private int expevIdentifier;
	private Date expDateFrom;
	private Date expDateTo;
	private String natureOfBusiness;
	private String travelInd;
	private String outOfStateInd;
	private int revisionNumber;
	private String actionCode;
	private Long adjIdentifier;
    private String deleteFlag;
    private Date paidDate;
    private Date OrigPaidDate;
    private String audit;
    private String actionCodeForDisplay;
	private String pdfInd;
    
	public ExpenseListBean() {}
	
	public ExpenseListBean(Long adjIdentifier, int apptIdentifier,
			String natureOfBusiness, int empIdentifier, Date expDateFrom,
			Date expDateTo, int expmIdentifier, int expevIdentifier, 
			String outOfStateInd, String actionCode,
			int revisionNumber, String status, String travelInd, String pdfInd) {
		this.adjIdentifier = adjIdentifier;
		this.apptIdentifier = apptIdentifier;
		this.natureOfBusiness = natureOfBusiness;
		this.empIdentifier = empIdentifier;
		this.expDateFrom = expDateFrom;
		this.expDateTo = expDateTo;
		this.expmIdentifier = expmIdentifier;
		this.outOfStateInd = outOfStateInd;
		this.revisionNumber = revisionNumber;
		this.actionCode = actionCode;
		this.travelInd = travelInd;
		this.expevIdentifier = expevIdentifier;
		this.pdfInd = pdfInd;
	}
	
	
	public String getAudit() {
		return audit;
	}

	public void setAudit(String audit) {
		this.audit = audit;
	}

	public int getExpevIdentifier() {
		return expevIdentifier;
	}

	public void setExpevIdentifier(int expevIdentifier) {
		this.expevIdentifier = expevIdentifier;
	}

	public int getApptIdentifier() {
		return apptIdentifier;
	}
	public void setApptIdentifier(int apptIdentifier) {
		this.apptIdentifier = apptIdentifier;
	}
	public int getEmpIdentifier() {
		return empIdentifier;
	}
	public void setEmpIdentifier(int empIdentifier) {
		this.empIdentifier = empIdentifier;
	}
	public int getExpmIdentifier() {
		return expmIdentifier;
	}
	public void setExpmIdentifier(int expmIdentifier) {
		this.expmIdentifier = expmIdentifier;
	}
	public Date getExpDateFrom() {
		return expDateFrom;
	}
	public void setExpDateFrom(Date expDateFrom) {
		this.expDateFrom = expDateFrom;
	}
	public Date getExpDateTo() {
		return expDateTo;
	}
	public void setExpDateTo(Date expDateTo) {
		this.expDateTo = expDateTo;
	}
	public Date getOrigPaidDate() {
		return OrigPaidDate;
	}
	public void setOrigPaidDate(Date OrigPaidDate) {
		this.OrigPaidDate = OrigPaidDate;
	}
	public String getTravelInd() {
		return travelInd;
	}
	public void setTravelInd(String travelInd) {
		this.travelInd = travelInd;
	}
	public String getOutOfStateInd() {
		return outOfStateInd;
	}
	public void setOutOfStateInd(String outOfStateInd) {
		this.outOfStateInd = outOfStateInd;
	}
	public int getRevisionNumber() {
		return revisionNumber;
	}
	public void setRevisionNumber(int revisionNumber) {
		this.revisionNumber = revisionNumber;
	}
	public String getActionCode() {
		return actionCode;
	}
	public void setActionCode(String actionCode) {
		this.actionCode = actionCode;
	}
	public Long getAdjIdentifier() {
		return adjIdentifier;
	}
	public void setAdjIdentifier(Long adjIdentifier) {
		this.adjIdentifier = adjIdentifier;
	}

	public void setDeleteFlag(String deleteFlag) {
		this.deleteFlag = deleteFlag;
	}

	public String getDeleteFlag() {
		return deleteFlag;
	}


	public void setPaidDate(Date paidDate) {
		this.paidDate = paidDate;
	}

	public Date getPaidDate() {
		return paidDate;
	}
	public String getNatureOfBusiness() {
		return natureOfBusiness;
	}

	public void setNatureOfBusiness(String natureOfBusiness) {
		this.natureOfBusiness = natureOfBusiness;
	}
	public String getActionCodeForDisplay() {
		return actionCodeForDisplay;
	}

	public void setActionCodeForDisplay(String actionCodeForDisplay) {
		this.actionCodeForDisplay = actionCodeForDisplay;
	}

	public String getPdfInd() {
		return pdfInd;
	}

	public void setPdfInd(String pdfInd) {
		this.pdfInd = pdfInd;
	}	
}
