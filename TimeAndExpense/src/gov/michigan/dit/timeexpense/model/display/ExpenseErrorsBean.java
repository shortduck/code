package gov.michigan.dit.timeexpense.model.display;

import java.io.Serializable;

public class ExpenseErrorsBean implements Serializable {
	private static final long serialVersionUID = 3949559279621281273L;
	
	private String source;
	private String code;
	private String desc;
	private String severity;
	
	public ExpenseErrorsBean() {}
	
	public ExpenseErrorsBean(String code, String desc, String severity, String source) {
		super();
		this.code = code;
		this.desc = desc;
		this.severity = severity;
		this.source = source;
	}

	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	
	
	
}
