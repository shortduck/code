package gov.michigan.dit.timeexpense.model.display;

import java.io.Serializable;

public class PcaBean implements Serializable {

	private static final long serialVersionUID = -1916974684065528461L;

	private int id;
	private String code = "";
	private String name = "";
	private String appr_year = "";
	private String agency = "";
	private String display = "";

	public PcaBean() {

	}

	public PcaBean(String appr_year, String code) {
		super();
		this.appr_year = appr_year;
		this.code = code;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDisplay() {
		return display;
	}

	public void setDisplay(String display) {
		this.display = display;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAgency() {
		return agency;
	}

	public void setAgency(String agency) {
		this.agency = agency;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the pca
	 */
	public String getcode() {
		return code;
	}

	/**
	 * @param pca
	 *            the pca to set
	 */
	public void setcode(String code) {
		this.code = code;
	}

	/**
	 * @return the appr_year
	 */
	public String getAppr_year() {
		return appr_year;
	}

	/**
	 * @param appr_year
	 *            the appr_year to set
	 */
	public void setAppr_year(String appr_year) {
		this.appr_year = appr_year;
	}

}
